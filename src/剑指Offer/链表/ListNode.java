package 剑指Offer.链表;

/**
 * @author 梦仙尘
 * @create 2020-03-28 20:45
 */
public class ListNode {
    int val;
    ListNode next;

    public ListNode(int val) {
        this.val = val;
    }

    @Override
    public String toString() {
        return "ListNode{" +
                "val=" + val +
                ", next=" + next +
                '}';
    }
}
